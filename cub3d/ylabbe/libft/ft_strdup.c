#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		src_size;
	char	*dup;
	int		i;

	i = 0;
	src_size = ft_strlen(s1);
	dup = malloc(sizeof(char) * src_size + 1);
	if (dup == NULL)
		return (NULL);
	while (s1[i])
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
